  const flagSk = document.getElementById("flag-sk");
const flagEn = document.getElementById("flag-en");

// Function to update text
function updateLanguage(lang) {
    const elements = document.querySelectorAll("[data-i18n]");
    elements.forEach(el => {
        const key = el.getAttribute("data-i18n");
        if (preklad[lang] && preklad[lang][key]) {
            el.innerText = preklad[lang][key];
        }
    });
}

// Flag click event listeners
flagSk.addEventListener("click", (e) => {
    e.preventDefault();
    updateLanguage("sk");
    flagSk.classList.add("d-none");
    flagEn.classList.remove("d-none");
});

flagEn.addEventListener("click", (e) => {
    e.preventDefault();
    updateLanguage("en");
    flagEn.classList.add("d-none");
    flagSk.classList.remove("d-none");
});
